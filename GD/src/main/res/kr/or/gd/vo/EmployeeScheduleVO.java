package kr.or.gd.vo;

public class EmployeeScheduleVO {

	private String sche_num;
	private String sche_name;
	private String sche_startdate;
	private String sche_enddate;
	private String sche_emp;
	public String getSche_num() {
		return sche_num;
	}
	public void setSche_num(String sche_num) {
		this.sche_num = sche_num;
	}
	public String getSche_name() {
		return sche_name;
	}
	public void setSche_name(String sche_name) {
		this.sche_name = sche_name;
	}
	public String getSche_startdate() {
		return sche_startdate;
	}
	public void setSche_startdate(String sche_startdate) {
		this.sche_startdate = sche_startdate;
	}
	public String getSche_enddate() {
		return sche_enddate;
	}
	public void setSche_enddate(String sche_enddate) {
		this.sche_enddate = sche_enddate;
	}
	public String getSche_emp() {
		return sche_emp;
	}
	public void setSche_emp(String sche_emp) {
		this.sche_emp = sche_emp;
	}
	
}
