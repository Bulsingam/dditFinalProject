package kr.or.gd.vo;

public class FormVO {


	private String form_num;
	private String form_name;
	private String form_data;
	private String form_exam;
	private String form_stan;
	public String getForm_num() {
		return form_num;
	}
	public void setForm_num(String form_num) {
		this.form_num = form_num;
	}
	public String getForm_name() {
		return form_name;
	}
	public void setForm_name(String form_name) {
		this.form_name = form_name;
	}
	public String getForm_data() {
		return form_data;
	}
	public void setForm_data(String form_data) {
		this.form_data = form_data;
	}
	public String getForm_exam() {
		return form_exam;
	}
	public void setForm_exam(String form_exam) {
		this.form_exam = form_exam;
	}
	public String getForm_stan() {
		return form_stan;
	}
	public void setForm_stan(String form_stan) {
		this.form_stan = form_stan;
	}
	
	
	
	
}
