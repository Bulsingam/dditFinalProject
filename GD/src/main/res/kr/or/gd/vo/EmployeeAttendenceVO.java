package kr.or.gd.vo;

public class EmployeeAttendenceVO {

	private String att_date;
	private String att_emp;
	private String att_apptime;
	private String att_daptime;
	public String getAtt_date() {
		return att_date;
	}
	public void setAtt_date(String att_date) {
		this.att_date = att_date;
	}
	public String getAtt_emp() {
		return att_emp;
	}
	public void setAtt_emp(String att_emp) {
		this.att_emp = att_emp;
	}
	public String getAtt_apptime() {
		return att_apptime;
	}
	public void setAtt_apptime(String att_apptime) {
		this.att_apptime = att_apptime;
	}
	public String getAtt_daptime() {
		return att_daptime;
	}
	public void setAtt_daptime(String att_daptime) {
		this.att_daptime = att_daptime;
	}
	
	
	
	
	
}
