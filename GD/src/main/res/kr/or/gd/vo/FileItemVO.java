package kr.or.gd.vo;

public class FileItemVO {
	private String file_seq;
	private String file_name;
	private String file_savename;
	private String file_type;
	private String file_size;
	private String file_postnum;
	private String file_sta;
	private String file_regdate;
	private String file_downhit;
	public String getFile_seq() {
		return file_seq;
	}
	public void setFile_seq(String file_seq) {
		this.file_seq = file_seq;
	}
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	public String getFile_savename() {
		return file_savename;
	}
	public void setFile_savename(String file_savename) {
		this.file_savename = file_savename;
	}
	public String getFile_type() {
		return file_type;
	}
	public void setFile_type(String file_type) {
		this.file_type = file_type;
	}
	public String getFile_size() {
		return file_size;
	}
	public void setFile_size(String file_size) {
		this.file_size = file_size;
	}
	public String getFile_postnum() {
		return file_postnum;
	}
	public void setFile_postnum(String file_postnum) {
		this.file_postnum = file_postnum;
	}
	public String getFile_sta() {
		return file_sta;
	}
	public void setFile_sta(String file_sta) {
		this.file_sta = file_sta;
	}
	public String getFile_regdate() {
		return file_regdate;
	}
	public void setFile_regdate(String file_regdate) {
		this.file_regdate = file_regdate;
	}
	public String getFile_downhit() {
		return file_downhit;
	}
	public void setFile_downhit(String file_downhit) {
		this.file_downhit = file_downhit;
	}
	
	
}
